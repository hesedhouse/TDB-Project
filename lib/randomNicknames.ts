const ALL: string[] = []
for (const adj of ADJECTIVES) {
  for (const noun of NOUNS) {
    ALL.push(`${adj} ${noun}`)
    if (ALL.length >= 120) break
  }
  if (ALL.length >= 120) break
}

/** 랜덤 닉네임 하나 반환 (최소 100개 이상 조합) */
export function getRandomNickname(): string {
  return ALL[Math.floor(Math.random() * ALL.length)] ?? '신난 찐빵'
}
